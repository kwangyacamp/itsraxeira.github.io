function sanitizeUTF8(text) {
    if (!text) return "";

    var output = "";
    for (var i=0; i<text.length; i++) {
        if (text.charCodeAt(i) <= 127 || text.charCodeAt(i) >= 160 && text.charCodeAt(i) <= 255) {
            output += text.charAt(i);
        }
    }
    return output.toLowerCase().replace(/\s/g, "-");
}

